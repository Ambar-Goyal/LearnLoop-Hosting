exports.getEnrolledCourses = async (req, res) => {
  try {
    const userId = req.user.id
    let userDetails = await User.findOne({
      _id: userId,
    })
      .populate({
        path: "courses",
        populate: {
          path: "courseContent",
          populate: {
            path: "subSection",
          },
        },
      })
      .exec()
    userDetails = userDetails.toObject()
    var SubsectionLength = 0
    for (var i = 0; i < userDetails.courses.length; i++) {
      let totalDurationInSeconds = 0
      SubsectionLength = 0
      for (var j = 0; j < userDetails.courses[i].courseContent.length; j++) {
        totalDurationInSeconds += userDetails.courses[i].courseContent[
          j
        ].subSection.reduce((acc, curr) => acc + parseInt(curr.timeDuration), 0)
        userDetails.courses[i].totalDuration = convertSecondsToDuration(
          totalDurationInSeconds
        )
        SubsectionLength +=
          userDetails.courses[i].courseContent[j].subSection.length
      }
      let courseProgressCount = await CourseProgress.findOne({
        courseID: userDetails.courses[i]._id,
        userId: userId,
      })

      if (SubsectionLength === 0) {
        userDetails.courses[i].progressPercentage = 100
      } else {
        // To make it up to 2 decimal point
        const multiplier = Math.pow(10, 2)
        userDetails.courses[i].progressPercentage =
          Math.round(
            (courseProgressCount / SubsectionLength) * 100 * multiplier
          ) / multiplier
      }
    }

    if (!userDetails) {
      return res.status(400).json({
        success: false,
        message: `Could not find user with id: ${userDetails}`,
      })
    }
    return res.status(200).json({
      success: true,
      data: userDetails.courses,
    })
  } catch (error) {
    return res.status(500).json({
      success: false,
      message: error.message,
    })
  }
}

// Controller function to fetch instructor dashboard data
exports.instructorDashboard = async (req, res) => {
  try {
    // 1Ô∏è‚É£ Fetch all courses created by the currently logged-in instructor
    // `req.user.id` is available because of authentication middleware
    
    const courseDetails = await Course.find({ instructor: req.user.id })

    // 2Ô∏è‚É£ Transform course data to include additional statistics
    const courseData = courseDetails.map((course) => {
      // Count total enrolled students for this course
      const totalStudentsEnrolled = course.studentsEnroled.length

      // Calculate total revenue generated from this course
      const totalAmountGenerated = totalStudentsEnrolled * course.price

      // Return a simplified course object with the stats included
      const courseDataWithStats = {
        _id: course._id, // Unique course ID
        courseName: course.courseName, // Name of the course
        courseDescription: course.courseDescription, // Short course description
        totalStudentsEnrolled, // Number of students enrolled
        totalAmountGenerated,  // Revenue (students √ó price)
        // üìù Note: You can add more fields here if needed for the dashboard
      }

      return courseDataWithStats
    })

    // 3Ô∏è‚É£ Send the processed data back to the frontend
    res.status(200).json({ courses: courseData })
  } catch (error) {
    // 4Ô∏è‚É£ Log the error and send a 500 response
    console.error(error)
    res.status(500).json({ message: "Server Error" })
  }
}
